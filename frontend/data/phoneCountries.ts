/** Format for display: "+27" or "+1-869" */
export function formatPhoneDialCode(dialCode: string): string {
  if (dialCode.startsWith('1') && dialCode.length > 1) {
    return `+1-${dialCode.slice(1)}`
  }
  return `+${dialCode}`
}

